# Data type in Python

#string
a = "hansen"
print (a)
print(type(a))

#int
b = 20
print (b)
print(type(b))

#float
c = 10.5
print (c)
print(type(c))

#complex
d = 1j
print (d)
print(type(d))

#list
e = ["a","b","c"]
print (e)
print(type(e))

#tuple
f = ("a","b","c")
print (f)
print(type(f))

# range
g = range(5)
print (g)
print(type(g))

# dict
h = {"nama" : "ani", "umur": 36}
print (h)
print(type(h))

# set
i = {"ani", "budi", "tono"}
print (i)
print(type(i))

# frozenset
j = frozenset({"ani","budi", "tono"})
print (j)
print(type(j))

# bool
k = True
print (k)
print(type(k))

# bytes
l = b"hello"
print (l)
print(type(l))

# byte array
m = bytearray(5)
print (m)
print(type(m))

# memoryview
n = memoryview(bytes(5))
print (n)
print(type(n))

# NoneType
o = None
print (o)
print(type(o))