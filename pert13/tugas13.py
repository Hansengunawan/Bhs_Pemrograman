class DATA:
    def __init__(self,Nama,Jurusan,TglLahir):
        self.nama=Nama
        self.jurusan=Jurusan
        self.tglLahir=TglLahir

a = DATA ("Hansen","Teknik Informatika","07-07-2003")
print("Nama         = ",a.nama)
print("Jurusan      = ",a.jurusan)
print("TglLahir     = ",a.tglLahir)
    
